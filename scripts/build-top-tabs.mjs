import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const tabsDir = path.join(rootDir, "workspace-tabs");
const outputFile = path.join(rootDir, "top-tabs", "tabs.generated.js");
const idPattern = /^[a-z0-9][a-z0-9-]*$/;

function toPosixPath(value) {
  return value.split(path.sep).join("/");
}

function resolveTabUrl(folderName, manifest) {
  const url = manifest.url || "index.html";
  if (/^(https?:)?\/\//.test(url) || url.startsWith("/") || url.startsWith("#")) {
    return url;
  }

  return toPosixPath(path.join("workspace-tabs", folderName, url));
}

function assertString(value, field, manifestPath) {
  if (typeof value !== "string" || value.trim() === "") {
    throw new Error(`${manifestPath}: "${field}" must be a non-empty string.`);
  }
  return value.trim();
}

async function readJson(filePath) {
  const raw = await fs.readFile(filePath, "utf8");
  try {
    return JSON.parse(raw);
  } catch (error) {
    throw new Error(`${filePath}: invalid JSON (${error.message}).`);
  }
}

async function collectTabs() {
  const entries = await fs.readdir(tabsDir, { withFileTypes: true }).catch((error) => {
    if (error.code === "ENOENT") return [];
    throw error;
  });
  const seen = new Set();
  const tabs = [];

  for (const entry of entries) {
    if (!entry.isDirectory() || entry.name.startsWith(".")) continue;

    const manifestPath = path.join(tabsDir, entry.name, "tab.manifest.json");
    const manifest = await readJson(manifestPath).catch((error) => {
      if (error.code === "ENOENT") return null;
      throw error;
    });
    if (!manifest) continue;

    const id = assertString(manifest.id, "id", manifestPath);
    if (!idPattern.test(id)) {
      throw new Error(`${manifestPath}: "id" must use lowercase letters, numbers, and hyphens.`);
    }
    if (seen.has(id)) {
      throw new Error(`${manifestPath}: duplicate tab id "${id}".`);
    }
    seen.add(id);

    tabs.push({
      id,
      label: assertString(manifest.label, "label", manifestPath),
      title: typeof manifest.title === "string" && manifest.title.trim() ? manifest.title.trim() : manifest.label.trim(),
      order: Number.isFinite(Number(manifest.order)) ? Number(manifest.order) : 999,
      folder: toPosixPath(path.join("workspace-tabs", entry.name)),
      url: resolveTabUrl(entry.name, manifest)
    });
  }

  return tabs.sort((a, b) => a.order - b.order || a.label.localeCompare(b.label, "zh-CN"));
}

async function main() {
  const tabs = await collectTabs();
  const manifestJson = JSON.stringify({ items: tabs }, null, 2).replace(/^/gm, "  ");
  const body = `// This file is generated by scripts/build-top-tabs.mjs. Do not edit by hand.
(function () {
  window.GlobalDataTopTabs = ${manifestJson.trimStart()};
})();
`;

  await fs.mkdir(path.dirname(outputFile), { recursive: true });
  await fs.writeFile(outputFile, body, "utf8");
  console.log(`Generated ${toPosixPath(path.relative(rootDir, outputFile))} with ${tabs.length} tab(s).`);
}

main().catch((error) => {
  console.error(error.message);
  process.exitCode = 1;
});
